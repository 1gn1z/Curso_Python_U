# SQL - Structured Query Language SQL es un lenguaje de dominio específico utilizado en programación, diseñado para administrar, 
# y recuperar información de sistemas de gestión de bases de datos relacionales.

# SQL es muy poderoso, pero puede ser laborioso se usar dia a dia, por que no esta muy relacionado con la programcion orientada
# a objetos

# Asi que para eso existen piezas de software como los ORM, que nos ayudan a trabajar con SQL.

# ORM - "OBJECT RELATIONAL MAPPING" -  MAPEO OBJETO-RELACIONAL

# Los ORM se encargan de abstraer los conceptos de SQL y presentarlos de una forma orientada a objetos

# Los OBJETOS se vuelven FILAS EN NUESTRA DB

# Las CLASES son MODULOS que se convierten en TABLAS DE NUESTRA DB

# Los ATRIBUTOS de las clases pasan a ser columnas de nuestra db
"""

                                    OBJETOS            ===         FILAS

                                    CLASES             ===         TABLAS

                                    ATRIBUTOS (CLASES) ===        COLUMNAS

"""

